function f = ELR(w)
mu = 0.03;
x = zeros([2,6]);
x(:,1) = [-1  -1];
x(:,2) = [0  1];
x(:,3) = [2  0];
x(:,4) = [0  -2];
x(:,5) = [1  0];
x(:,6) = [2  -1];
Z = [x; x(1,:).^2;x(1,:).*x(2,:); x(2,:).^2; ones(1,6)];
Zz = [Z(:,1:3) -Z(:,4:6)];
P = size(x,2);
f = sum(log(1+exp(-Zz'*w)))/P + 0.5*mu*w'*w;

end